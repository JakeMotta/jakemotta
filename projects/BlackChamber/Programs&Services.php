It Takes a Village with STEM: (picture & donate button)
Program that connects students to STEM using creative methods and technics. Professionals volunteer to teach Junior Achievement pre-set courses in STEM related subjects to youth from 1st - 12th grade who may not have had an interest in STEM at first. This Introductory method we believe will ignite the interest of students to further explore STEM related subjects and careers. “It Takes a Village” concept implies the use of local community professionals and volunteers as instructors and supporters. (more, link to bottom of page )

<hr>

