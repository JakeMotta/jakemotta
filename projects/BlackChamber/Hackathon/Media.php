        <span id="mediumText" class="bold">SAN JOSE / SAN FRANCISCO BAY AREA, CA:</span> The Silicon Valley Black Chamber of Commerce
        (SVBCC) / Center for Entrepreneurial Development is excited to announce that it is a <em>PLAY 60</em>,
        <em>Play On</em> grant winner! <em>PLAY 60</em>, <em>Play On</em> is an initiative of the Super Bowl 50 Legacy Fund, the
        NFL Foundation and guided by KaBoom! to help combat childhood obesity and to build creative
        and innovative neighborhood projects that will make balanced and active play more easily
        accessible so kids can thrive.<br><br>
        
        The generous support will enable the Chamber to host several <em>PLAY 60</em>, <em>Play On</em> challenge events
        throughout the San Francisco Bay Area, designed to encourage creativity in solving problems and
        using active play and movement to be more physically fit.<br><br>
        
        In partnership with local non-profit organizations and Silicon Valley businesses, kids of all ages,
        families and young adults are invited to participate in challenge events over the coming weeks.<br><br><br><br>
        
        <hr>
        
        <span id="mediumText" class="bold">Silicon Valley Black Youth Hack-a-Thon:</span>
        The first event is the Silicon Valley Black Youth Hack-a-Thon to take place January 16-31, 2016 at
        GSVlabs (425 Broadway Street, Redwood City, CA). The event will bring together more than 150
        talented African American youth and young adults, ages 16 to 24, from Bay Area high schools,
        colleges, clubs and companies for an opportunity to compete in a technology challenge. Hack-a-
        Thon participants can compete for cash prizes, awards and scholarships. Young people who wish
        to be competitive “hackers” must complete the <a class="bold" href="https://events.r20.constantcontact.com/register/eventReg?oeidk=a07ebrtxmhx50a81291&oseq=&c=&ch=">online registration form</a> by January 14th.<br><br><br><br>
        
        <hr>
        
        <span id="mediumText" class="bold">IDEAS IN MOTION – <em>PLAY 60</em>, <em>Play On</em> Challenges</span>
            <span class="shortTab">
                January 16, 2016 Dr. Martin Luther King Library in San Jose-
                January 23, 2016 UC Berkeley, Upper Sproul Plaza
                January 31, 2016 East Palo Alto YMCA
            </span><br><br>
        
        <em>For more information about the <em>PLAY 60</em>, <em>Play On</em> challenge events, visit the Silicon Valley Black
        Chamber of Commerce website at <a class="bold" href="http://www.blackchamber.com/hackathon">www.blackchamber.com/hackathon</a>.</em><br><br>
        
        The Chamber’s events are inspired by the Super Bowl 50 Host Committee’s challenge to local nonprofits to share with the world the unique neighborhoods, culture, activities and spirit of the San Francisco Bay Area and Silicon Valley. More than 400 youth from around the San Francisco Bay Area will participate in the SVBCC challenge events.<br><br>
        
        The Silicon Valley Black Chamber of Commerce is proud to partner with Bay Area Tutoring Association, Rocket Fuel Inc., City of San Jose, City of East Palo Alto and UC Berkeley. The events bring together an all-star list of national sponsors including Black Enterprise, AT&T, Intel Corporation, UNCF and #YESWECODE. The Chamber has also garnered pledges of support and collaboration from other Northern California Black Chambers of Commerce and local organizations supporting youth including Street Code Academy, California Alliance of African American Educators, Santa Clara County Alliance of Black Educators, National Black MBA Association, Jack and Jill of America, and Codewritingkids.com.<br><br>
        
        Carl Davis Jr., President of Silicon Valley Black Chamber of Commerce states, “The employer diversity data in the technology sector is a clear indicator of the opportunities ahead. The current generation of youth and young adults of African descent will direct a significant portion of the cultural landscape of Silicon Valley and the San Francisco Bay Area. It is our duty to find innovative ways to prepare them for 2020 and beyond.”<br><br><br><br>
        
