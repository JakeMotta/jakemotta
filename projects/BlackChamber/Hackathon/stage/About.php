     	<span id="title">Why is the SVBCC facilitating a Hack-A-Thon For African American young adults?</span>
        <hr>
        
        <div class="tabbed"><span id="text">
It is imperative we prepare future generations to thrive in this new global economy and society. The Silicon Valley Black Chamber of Commerce exists for this purpose. We create, identify and/or expand economic development opportunities within the African and African American community of Silicon Valley. This includes, but is not limited to, direct employment, job training, referrals, education and entrepreneurial opportunities.<br><br>

The Silicon Valley is a mecca for entrepreneurship and technology. However diversity statistics of many Silicon Valley companies leading the world have very few people of African or African American descent leading or employed within their organizations. Regional education data also shows there are very few African or African American students enrolled in computer science or computer related courses in high school or college.<br><br>

By facilitating this annual hack-a-thon for African and African American young adults as an extension of our Next Generation Business Academy program, we will increase the entrepreneurial, employment and academic awareness of computer technology related activities within our own Silicon Valley and across the nation.<br><br>

		</span></div>

		<br><br>
    
        <span id="title">Next Generation Business Academy:</span>
        <hr>
        
        <div class="tabbed"><span id="text">
            
            <img id="image" src="images/pic-youth4.jpg"/>
            
            Since 2004 the Silicon Valley Black Chamber of Commerce and the Center for Entrepreneurial Development has provided innovated ways to introduce entrepreneurship to young and upcoming adults, many of whom are from low-to-moderate income families.<br><br>
            
            Our Next Gen program teaches financial life skills using business and financial literacy, to high school students of all ethnicities. Students learn how to write business and marketing plans, create financial projections, develop commercials and even how to pitch their products and services. All of this to become better equipped to start, operate and grow a business.<br><br>

			
        </span></div>
        
        <br><br>
	
        <span id="title">Silicon Valley’s Black Youth Hack-A-Thon:</span>
        <hr>
        
        <div class="tabbed"><span id="text">
            An extension of our Next Generation Business Academy, this year’s “Silicon Valley’s Black Youth hack-a-thon” is a codefest of problem solving in a real world situation. Youth (16-24) from high school, college or local clubs come together, get to know one another, get assigned to teams and take on the task of producing a software application solution (an App) that satisfies the objective. <br><br>
            
            The winning teams receive combinations of cash, scholarships and/or awards. Your presence and contributions as a sponsor help provide the above.<br><br>

            This hack-a-thon is unique in that it spans multiple weekends and culminates February 6th, 2016, Super Bowl Saturday at Rocket Fuel Inc. in Redwood City, CA. with celebrity guests and speakers invited to motivate the hundreds of young attendees.<br><br>
            
           Friday November 13th, 2015 at San Jose State University, Saturday November 14th at the SF 49er Academy in East Palo Alto and Saturday November 21st (Oakland) starts the recruitment of the youth who will participate in the hack-a-thon. Once the teams are assembled the real work begins. Designing, testing and then preparing their presentation showing their creative solution will challenge the team’s ability to work together and determine Who, How and What to present.<br><br>
    
            Saturday February 6th is the presentation day where judges will decide which solution best meets the objective in the most creative way. Other students from around the Bay Area are invited to participate and see these creative presentations with the hopes of stimulating an interest to get them engaged in STEM. We know not only the jobs of the future but the entrepreneurs of the future will need to possess some knowledge in the Science, Technology, Engineering and Math disciplines.<br><br>

        </span></div>   
        
        <br><br>
        
        <span id="title">Mission:</span>
        <hr>
        
        <div class="tabbed"><span id="text">
            Our mission is to help transform children's lives through education and social innovation using technology.<br><br>
            
            We want to act as an incubator that harnesses the interests of high potential youth from LMI neighborhoods and leads them into the plethora of STEM career opportunities awaiting them.<br><br>
            
            We are proudly committed to providing a fun and playful learning experience for all those attending coders and or observers. Instead of our youth being passive observers and avid users of technology, we instill confidence and encourage them to create and design the games and apps that others will use tomorrow. Our month long campaign will garner local, regional and national attention and culminates with showcasing the achievements of these youth, many from LMI families. Wouldn’t it be wonderful if these youth are now on a path to becoming world-class coders? By learning this highly valuable and relevant 21st century skill, these young people can become part of the 120 million high-skilled job applicants needed by 2020.<br><br>
        
