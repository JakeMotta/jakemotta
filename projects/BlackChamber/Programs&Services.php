Programs:
Next Generation Business Academy: (picture & donate button)
Since 2004 our Next Gen program has taught business and financial literacy to high school students of all ethnicities. Students learn how to write business and marketing plans, create financial projections, develop commercials and even how to pitch their products and services. All of this to become better equipped to start, operate and grow a business.  (more, link to bottom of page )

