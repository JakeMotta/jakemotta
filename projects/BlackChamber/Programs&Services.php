Small Business - Incubator Program: (picture & donate button) 
We help businesses prepare by providing the tools, know-how  and technical assistance to launch them towards success. We provide low cost rental office space, free parking, use of conference facilities and Wi-Fi internet, along with a customized set of curriculum for their specific business. We help them prepare strategies for growth, finance and how to pitch their product or service. We can even help them with their exit strategy. All of this as part of our small business services - Incubator program. (more, link to bottom of page )

